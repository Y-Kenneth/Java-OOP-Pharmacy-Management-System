    private void doSearchApoteker(){
        if(searchApotekerInputTextField.getText().isEmpty()){
            return;
        }
            
        // Nanti akan ditentukan sendiri di Control, inputannya String atau Integer
        a = ac.searchApotekerByAll(searchApotekerInputTextField.getText());
        
        if(a != null){
            setEditDeleteButtonApoteker(true);
            clearTextApoteker();

            namaApotekerInputTextField.setText(a.getNama_apoteker());
            gajiInputTextField.setText(Integer.toString(a.getGaji()));
            emailApotekerInputTextField.setText(a.getEmail());

            idApoteker = a.getId_apoteker();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Apoteker Tidak Ditemukan!");
        }
    }
    
