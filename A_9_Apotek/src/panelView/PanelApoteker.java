    private void simpanApotekerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanApotekerButtonActionPerformed
        // TODO add your handling code here:
        if(actionApoteker == null)
        return;
        try{
            inputKosongApotekerException();
            
            int dialog = JOptionPane.showConfirmDialog(rootPane, "Yakin ingin menambahkan Apoteker?");
            if(dialog != JOptionPane.YES_OPTION) return;

            switch(actionApoteker){
                case "add":
                    a = new Apoteker(namaApotekerInputTextField.getText(), emailApotekerInputTextField.getText(), Integer.parseInt(gajiInputTextField.getText()));
                    ac.insertDataApoteker(a);
                    clearTextApoteker();
                    setEditDeleteButtonApoteker(false);
                    setComponentsApoteker(false);
                    break;
                case "update":
                    a = new Apoteker(namaApotekerInputTextField.getText(), emailApotekerInputTextField.getText(), Integer.parseInt(gajiInputTextField.getText()));
                    ac.updateDataApoteker(a, idApoteker);
                    clearTextApoteker();
                    setEditDeleteButtonApoteker(false);
                    setComponentsApoteker(false);
                    break;
                default:
                    break;
            }
            showApoteker();
            actionApoteker = null;
        }catch(InputKosongException e){
            JOptionPane.showMessageDialog(rootPane, e.toString());
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(rootPane, "Input Gaji Harus Berupa Angka!");
        }
    }//GEN-LAST:event_simpanApotekerButtonActionPerformed

