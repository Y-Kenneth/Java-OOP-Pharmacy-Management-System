    // EXCEPTION
    public void loginGagalException(Apoteker A, String inputUsername, String inputPassword) throws LoginGagalException{
        if (A == null || !A.getUsername().equals(inputUsername) || !A.getPassword().equals(inputPassword))
        {
            // Jika username atau password tidak sesuai dengan yang dimiliki oleh Apoteker A
            throw new LoginGagalException();
        }
    }
    
