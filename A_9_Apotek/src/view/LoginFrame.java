    // Logika percobaan login
    private void attemptLogin() {
        
        String selectedNamaApoteker = (String) namaApotekerDropdown.getSelectedItem();
        String username = usernameInputTextField.getText();
        String password = new String(passwordInputPasswordField.getPassword()); // Ambil password dari JPasswordField

        Apoteker selectedApoteker = null;
        // Cari objek Apoteker lengkap berdasarkan nama yang dipilih dari list
        for (Apoteker apoteker : listApoteker) {
            if (apoteker.getNama_apoteker().equals(selectedNamaApoteker)) {
                selectedApoteker = apoteker;
                break;
            }
        }

        if (selectedApoteker == null) {
            JOptionPane.showMessageDialog(this, "Apoteker tidak ditemukan di daftar. Harap refresh.", "Error Login", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            Apoteker apoteker = ac.searchApotekerById(selectedApoteker.getId_apoteker());
            loginGagalException(apoteker, username, password);
            
            JOptionPane.showMessageDialog(this, "Selamat datang, Apoteker " + apoteker.getNama_apoteker() + 
                    "!", "Login Berhasil", JOptionPane.INFORMATION_MESSAGE);
            
            // Hapus semua komponen yang saat ini ada di JFrame (komponen UI)
            this.dispose();

            ApotekMainFrame mainAppFrame = new ApotekMainFrame(apoteker);
            mainAppFrame.setVisible(true);
        } catch (LoginGagalException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Login Gagal", JOptionPane.ERROR_MESSAGE);
            passwordInputPasswordField.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan sistem: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    
